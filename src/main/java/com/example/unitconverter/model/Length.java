package com.example.unitconverter.model;

public class Length {

    private Double mile;
    private Double kilometer;
    private Double yard;
    private Double meter;
    private Double inch;
    private Double foot;
    private Double centimeter;
    private Double millimeter;

    public Length(){};

    public Double getMile() {
        return this.mile;
    }

    public void setMile(Double mile) {
        this.mile = mile;
    }

    public Double getKilometer() {
        return this.kilometer;
    }

    public void setKilometer(Double kilometer) {
        this.kilometer = kilometer;
    }

    public Double getYard() {
        return this.yard;
    }

    public void setYard(Double yard) {
        this.yard = yard;
    }

    public Double getMeter() {
        return this.meter;
    }

    public void setMeter(Double meter) {
        this.meter = meter;
    }

    public Double getInch() {
        return this.inch;
    }

    public void setInch(Double inch) {
        this.inch = inch;
    }

    public Double getFoot() {
        return this.foot;
    }

    public void setFoot(Double foot) {
        this.foot = foot;
    }

    public Double getCentimeter() {
        return this.centimeter;
    }

    public void setCentimeter(Double centimeter) {
        this.centimeter = centimeter;
    }

    public Double getMillimeter() {
        return this.millimeter;
    }

    public void setMillimeter(Double millimeter) {
        this.millimeter = millimeter;
    }
    
}